public class Condicional {
    public static void main(String[] args) {
        
        // ===========
        //  Variáveis
        // ===========
        int idade = 15;

        // ------------
        System.out.println("===========");

        // =============
        //  Condicional
        // ============
        if (idade >= 18) {
            System.out.println("Pode entrar, bom filme!");
        } else {
            System.out.println("Esse filme é apenas para maiores, criança!");
        }
        System.out.println("===========");
    }
}
