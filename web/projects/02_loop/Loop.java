public class Loop {
    public static void main(String[] args) {
        
        // ==================================
        //  LAÇO DE REPETIÇÃO (LOOP) 
        // ==================================
        System.out.println("===========");
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }
        System.out.println("===========");
    }
}
